                    <h2> ¿Qué es? </h2>
                    <p>La carrera de Ingeniería en Sistemas Automotrices es una carrera de nueva creación que se imparte a través de una red académica colaborativa formada por ocho unidades académicas del Instituto Politécnico Nacional. Esta carrera se diferencia de las demás, porque cuenta con el concepto de movilidad de alumnos y docentes.
                        La carrera tiene una duración de nueve semestres, a partir del séptimo semestre algunas asignaturas optativas se impartirán en inglés, en el noveno semestre el alumno realizará una estancia industrial o un proyecto terminal con la finalidad por titularse.
                        </p>
