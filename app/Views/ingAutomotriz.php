                    <h2> Perfil del aspirante </h2>
                    <p>
                        
                        El aspirante a la carrera de Ingeniería en Sistemas Automotrices deberá tener las siguientes cualidades:
                        <ul>
                        <li>Contar con bases académicas sólidas en física y matemáticas del nivel educativo antecedente.</li>
                        <li> Aplicar las herramientas de matemáticas, física y computación.</li>
                        <li>Analizar, sintetizar y evaluar información.</li>
                        <li>Integrar y aplicar los conocimientos adquiridos.</li>
                        <li>Trabajar de manera individual y en grupo.</li>
                        <li>Interés por observar e investigar los fenómenos naturales y su efecto hacia el entorno social.</li>
                        <li>Interés por conocer y participar en la solución de la problemática nacional.</li>
                        <li>Actitud creadora para transformar la realidad mediante programas y proyectos de innovación tecnológica.</li>
                        <li>Habilidad para la comprensión y traducción de textos en idioma extranjero (inglés).</li>
                    </ul></p>
