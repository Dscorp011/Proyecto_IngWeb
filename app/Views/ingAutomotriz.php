                    <h2> objetivos </h2>
                    <p>
                        <ul>
                        <li>EL egresado en ISA es capaz de elaborar proyectos y solucionar problemas en la industria utilizando los conocimientos adquiridos en ciencias exactas y ciencias de la ingeniería.</li> 
                        <li>El egresado en ISA propone mejoras en los procesos industriales a través de la manufactura avanzada, los sistemas mecánicos y la automatización.</li>
                        <li>El egresado ISA diseña procesos de manufactura y productos a través de las herramientas CAD, CAM y CAE.</li>
                        <li>El egresado ISA participa en proyectos de Innovación y Desarrollo Tecnológico y reconoce la necesidad de una formación profesional continua.</li>
                        </ul> </p>
