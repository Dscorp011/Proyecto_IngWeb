                    <p>
                        
                        El aspirante a la carrera de Ingeniería en electronica 
                        <ul>
                            
                        <li>Tener los conocimientos en física, matemáticas, lógica, química, estadística y metodología de la investigación del nivel medio superior o equivalente.</li>
