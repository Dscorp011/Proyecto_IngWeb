                    <h2> objetivos </h2>
                    <p>
                        El objetivo de la carrera es formar Ingenieros en Electrónica con un alto sentido de responsabilidad, de ética y de servicio. El Ingeniero Electrónico es el profesional que posee los conocimientos específicos, actitudes, habilidades intelectuales, verbales, numéricas, abstractas, acordes al programa educativo para planear, diseñar, evaluar, construir, instalar, integrar, operar, administrar, y mantener sistemas tanto analógicos como digitales para el desarrollo, el control, las comunicaciones y la instrumentación, haciendo uso de software especializado a través de diferentes estrategias y actividades prácticas, buscando el mejor aprovechamiento de los recursos sin deterioro del ambiente en beneficio de la sociedad </p>
